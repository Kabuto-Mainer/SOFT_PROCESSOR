#ifndef MY_LIST_H
#define MY_LIST_H

#include "list-config.h"
#include "list-const.h"
#include "list-func.h"
#include "list-type.h"

// Covers of fuctions for simply use
// -------------------------------------------------------------------------------------------------------
#define listCreate(list)                                        \
    if ((#list)[0] == '&')                                      \
    {                                                           \
        list_ctr_main(list, __FILE__, __LINE__, &((#list)[1])); \
    }                                                           \
                                                                \
    else                                                        \
    {                                                           \
        list_ctr_main(list, __FILE__, __LINE__, #list);         \
    }
// -------------------------------------------------------------------------------------------------------
#define listDestroy(list) \
    list_dtr(list)
// -------------------------------------------------------------------------------------------------------

// -------------------------------------------------------------------------------------------------------
#define pushDump(list) \
    list_dump(list, "Just Dump")
// -------------------------------------------------------------------------------------------------------

// -------------------------------------------------------------------------------------------------------
#define insertAfter(list, index, value) \
    list_insert_after(list, index, value, __FILE__, __LINE__, "insertAfter")
// -------------------------------------------------------------------------------------------------------
#define deleteCurrent(list, index) \
    list_delete_current(list, index, __FILE__, __LINE__, "deleteCurrent")
// -------------------------------------------------------------------------------------------------------

// -------------------------------------------------------------------------------------------------------
#define getHead(list) \
    list_head(list)
// -------------------------------------------------------------------------------------------------------
#define getNext(index) \
    list_next(index)
// -------------------------------------------------------------------------------------------------------
#define getPrev(index) \
    list_prev(index)
// -------------------------------------------------------------------------------------------------------
#define getValue(index) \
    list_data(index)
// -------------------------------------------------------------------------------------------------------


#endif // MY_LIST_H
