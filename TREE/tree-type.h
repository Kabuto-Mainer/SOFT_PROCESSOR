// Type of variables, what can be saved in tree
// -------------------------------------------------------------------------------------------------------
typedef int trv_t;
// -------------------------------------------------------------------------------------------------------
#define FORMAT_TRV "%d"
// -------------------------------------------------------------------------------------------------------
const trv_t FREE_VALUE = INT_MAX;
// -------------------------------------------------------------------------------------------------------

// Struct of node
// -------------------------------------------------------------------------------------------------------
struct node_t
{
    char bird;
    int amount;
    trv_t value;
    node_t* left;
    node_t* right;
    node_t* root;
};
// -------------------------------------------------------------------------------------------------------
struct create_inf
{
    const char* file;
    int line;
    const char* name;

    comand_inf_t* cmd_inf;
    int number_dir;
};
// -------------------------------------------------------------------------------------------------------
struct error_inf
{
    int error;
    const char* file;
    int line;
    const char* func;
};
// -------------------------------------------------------------------------------------------------------
struct tree_t
{
    node_t* root;
    size_t size;

    create_inf create;
    error_inf error;
};
// -------------------------------------------------------------------------------------------------------

